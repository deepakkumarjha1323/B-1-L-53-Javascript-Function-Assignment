// Functions

// Addition
// Function Defination
function add(a,b){
    console.log(a+b);
}
// Function Invoke/Call
add(20,10);

// Subtraction
// Function defination
function Subtraction(c,d){
    //Function Body
    // Every Function Return somethigs
    return c - d;
}
// Function Calling/Invoking
var sub = Subtraction(30,15);
console.log(sub);

